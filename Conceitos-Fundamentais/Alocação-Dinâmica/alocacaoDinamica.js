// Exemplo de alocação dinâmica em JavaScript

// Criando um array vazio
let numeros = [];

// Adicionando elementos ao array dinamicamente
numeros.push(10);  // O array tem agora 1 elemento
numeros.push(20);  // O array tem agora 2 elementos
numeros.push(30);  // O array tem agora 3 elementos

console.log("Array após adicionar elementos:", numeros);

// Removendo um elemento do array dinamicamente
numeros.pop();  // O array tem agora 2 elementos

console.log("Array após remover um elemento:", numeros);